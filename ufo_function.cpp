std::string end_game(std::string answer, std::string codeword){
  if (answer == codeword){
    return "Hooray! You saved the person and earned a medal of honor!";
  }
  else{
    return "Oh no! The UFO just flew away with another person!";
  }
}

void display_status(std::vector<char> incorrect, std::string answer){
    std::cout << "Incorrect Guesses: ";
    for (int a = 0; a < incorrect.size(); a++){
      std::cout << incorrect[a] << " ";
    }
    std::cout << "\n";

    std::cout << "Codeword: ";
    for (int b = 0; b < answer.size(); b++){
      std::cout  << answer[b] << " ";  
    }
}

